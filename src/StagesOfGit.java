public class StagesOfGit {
    public static void main(String[] args) {
        System.out.println("1. Modified :means that you have changed the file but have not committed it to your database yet.");
        System.out.println("2. Staged :means that you have marked a modified file in its current version to go into your next commit");
        System.out.println("3. Committed :means that the data is safely stored in your local database.");
    }
}
